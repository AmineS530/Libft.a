char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*uwu;

	uwu = ((char *)s);
	i = 0;
	while (uwu[i] != c && uwu[i] != '\0')
	{
		if (uwu[i] == c)
		{
			return (&uwu[i]);
		}
		i++;
	}
	if (uwu[i] == c)
	{
		return (&uwu[i]);
	}
	return (0);
}
